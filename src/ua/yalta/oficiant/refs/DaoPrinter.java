public  class DaoPrinter {

    String code;
    String npname;
    String ip;
    String port;

    public DaoPrinter() {

    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getNpname() {
        return npname;
    }

    public void setNpname(String npname) {
        this.npname = npname;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}
